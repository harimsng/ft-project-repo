int	ft_str_is_numeric(char *str)
{
	if (str == 0)
		return (0);
	while (1)
	{
		if (*str == 0)
			return (1);
		else if (*str < '0' || *str > '9')
			return (0);
		++str;
	}
	return (1);
}
