long long	ft_long_add(long a, long b)
{
	return (a + b);
}

long long	ft_long_sub(long a, long b)
{
	return (a - b);
}

long long	ft_long_mul(long a, long b)
{
	return (a * b);
}

long long	ft_long_div(long a, long b)
{
	return (a / b);
}

long long	ft_long_mod(long a, long b)
{
	return (a % b);
}
