int	ft_is_prime(int nb)
{
	int		denom;

	if (nb % 2 == 0 || nb < 2)
		return (0);
	denom = 3;
	while (denom <= 46341)
	{
		if (nb % denom == 0)
			return (0);
		denom += 2;
	}
	return (1);
}
