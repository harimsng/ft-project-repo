#include "libft.h"

//1000 0000 0000 0000 0000 0000 0000 0000 = -2147483648
//0000 0000 0000 0000 0000 0000 0111 1111 = 127
//1111 1111 1111 1111 1111 1111 1000 0000 = ~127
//1111 1111 1111 1111 1111 1111 1000 0000 = -128
//0000 0000 0000 0000 0000 0000 0111 1110 = ~(-128 + 1)
//1111 1111 1111 1111 1111 1111 1111 1111 = -1
//1111 1111 1111 1111 1111 1111 1111 1111 = ~(-1 + 1)
//1111 1111 1111 1111 1111 1111 0111 1111 = -129
//0000 0000 0000 0000 0000 0000 0111 1111 = ~(-129 + 1)
int	ft_toupper(int c)
{
	if (!(~c & ~127) && c != -1)
		c = (c | 128) & 255;
	return (c - 32 * (c > 96 && c < 123));
}
