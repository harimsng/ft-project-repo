int	ft_atoi(const char *str)
{
	register unsigned long long	num;
	const char			*temp;
	int					neg;

	while (table1[(int)*str])
		++str;
	neg = (*str == '-');
	str += (table1[(int)*str] == 2);
	num = 0;
	temp = str;
	while (table2[(int)*str])
		num = num * 10 + *str++ - 48;
	if (str - temp > 20 || num > LONG_MAX)
		return ((int)(LONG_MAX * !neg + LONG_MIN * neg));
	return ((1 - 2 * neg) * ((int)num));
}
